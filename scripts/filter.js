$(document).ready(function(){
    $('.listing>li').click(function(){
        var sel_item = $(this).html();
    $('.box').hide();
    $('.box').each(function(){
        if($(this).hasClass(sel_item)){
            $(this).animate({"width":"toggle"});
            }
    });
    });
    
    
});  

